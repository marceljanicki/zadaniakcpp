#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int a = 10, b = 3;
    double c = 3.14, d = 2.71;

    printf("Wartosci zmiennych w C:\n");
    printf("a = %d, b = %d, c = %f, d = %f\n", a, b, c, d);

    cout << "Wartosci zmiennych w C++:" << endl;
    cout << "a = " << a << ", b = " << b << ", c = " << c << ", d = " << d << endl;

    return 0;
}
